package day03;

public class C02Increment {
    public static void main(String[] args) {
//Increment -->arttÄ±rma
        int numA = 2;
//        numA = numA + 3;
        numA += 3;
        System.out.println("numA = " + numA);

        int numB = 10;
//        numB = numB * 7;
        numB*=7;
        System.out.println("numB = " + numB);

        int numC=7;
        numC++;
        System.out.println("numC = " + numC);

        int numD=11;
        numD++;
        System.out.println("numD = " + numD);
    }
}
