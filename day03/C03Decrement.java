package day03;

import java.util.Scanner;

public class C03Decrement {
    public static void main(String[] args) {
        //Decrement-->Azaltma
        int numA = 2;
//        numA=numA-3;
        numA -= 3;
        System.out.println("numA = " + numA);

        int numB = 20;
//        numB=numB/5;
        numB /= 5;
        System.out.println("numB = " + numB);

        int numD = 7;
        numD--;
        System.out.println("numD = " + numD);

        int numE = 11;
        numE--;
        System.out.println("numE = " + numE);

    }
}
